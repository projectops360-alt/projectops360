#!/usr/bin/env node
// ============================================================================
// ProjectOps360° — App Screens knowledge seed generator
// ============================================================================
// Reads the bilingual screen sheets in docs/app-knowledge/screens/*.md and
// emits idempotent, version-aware SQL for the Knowledge OS corpus
// (knowledge_packages / _package_versions / _localizations / _chunks).
//
// Behavior per package (slug):
//   - Package upserted (global scope, domain 'app_screens', status 'published').
//   - If the current version's EN/ES title+body already match the sheet,
//     NOTHING happens (safe to re-run).
//   - If content changed (or no version exists): the current version is
//     demoted (is_current=false), its chunks are soft-deleted, and a NEW
//     version (version_no+1, tier 'learned_pattern') is inserted with fresh
//     EN/ES localizations and chunks (index_status='pending' → the existing
//     indexer embeds them; lexical search works immediately).
//
// Usage:
//   node scripts/generate-app-screens-seed.mjs                 # SQL → stdout
//   node scripts/generate-app-screens-seed.mjs --out seed.sql  # SQL → file
//   node scripts/generate-app-screens-seed.mjs --check         # stale sheets (CI)
//   node scripts/generate-app-screens-seed.mjs --update-hashes # record source hashes
//
// --check compares sha256 of each sheet's `sources:` files against
// docs/app-knowledge/source-hashes.json. Exit 1 if any sheet is stale →
// regenerate that sheet (see docs/app-knowledge/README.md) and re-run
// --update-hashes. Wire it as a CI step to keep Isabella's screen knowledge
// in sync with the code.
// ============================================================================

import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { createHash } from "node:crypto";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, "..", "..", ".."); // repo root (docs/app-knowledge/scripts → root)
const SCREENS_DIR = resolve(HERE, "..", "screens");
const HASHES_FILE = resolve(HERE, "..", "source-hashes.json");

const DOMAIN = "app_screens";
const TIER = "learned_pattern"; // auto-generatable per Knowledge OS governance; promote after human review

// ── Parse a screen sheet ────────────────────────────────────────────────────
function parseSheet(file) {
  const raw = readFileSync(file, "utf8").replace(/\r\n/g, "\n");
  const fm = raw.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!fm) throw new Error(`${file}: missing frontmatter`);
  const [, head, body] = fm;

  const get = (key) => (head.match(new RegExp(`^${key}:\\s*(.+)$`, "m")) || [])[1]?.trim();
  const slug = get("slug");
  const route = get("route");
  const sources = [...head.matchAll(/^\s+-\s+(.+)$/gm)].map((m) => m[1].trim());
  if (!slug || !route) throw new Error(`${file}: missing slug/route`);

  const en = body.match(/^# EN:\s*(.+)\n([\s\S]*?)(?=^# ES:)/m);
  const es = body.match(/^# ES:\s*(.+)\n([\s\S]*)$/m);
  if (!en || !es) throw new Error(`${file}: missing "# EN:" or "# ES:" section`);

  return {
    slug,
    route,
    sources,
    sourceRef: `${route} — generated from: ${sources.join(", ")}`,
    en: { title: en[1].trim(), body: en[2].trim() },
    es: { title: es[1].trim(), body: es[2].trim() },
  };
}

function loadSheets() {
  return readdirSync(SCREENS_DIR)
    .filter((f) => f.endsWith(".md"))
    .sort()
    .map((f) => parseSheet(join(SCREENS_DIR, f)));
}

// ── SQL emission ────────────────────────────────────────────────────────────
const q = (s) => `'${String(s).replace(/'/g, "''")}'`;

function buildSql(sheets) {
  const dataRows = sheets
    .map(
      (s) =>
        `    (${q(s.slug)}, ${q(s.en.title)}, ${q(s.en.body)}, ${q(s.es.title)}, ${q(s.es.body)}, ${q(s.sourceRef)})`,
    )
    .join(",\n");

  return `-- ============================================================================
-- ProjectOps360° — Isabella App Screens corpus (GENERATED — do not edit)
-- Generated by docs/app-knowledge/scripts/generate-app-screens-seed.mjs
-- Idempotent + version-aware: unchanged packages are untouched; changed ones
-- get a new current version and re-pending chunks. Requires pgcrypto (digest).
-- ============================================================================
WITH data(slug, title_en, body_en, title_es, body_es, source_ref) AS (
  VALUES
${dataRows}
),
ins_pkg AS (
  INSERT INTO public.knowledge_packages (organization_id, slug, domain, status, default_language)
  SELECT NULL, d.slug, ${q(DOMAIN)}, 'published', 'en' FROM data d
  ON CONFLICT DO NOTHING
  RETURNING id, slug
),
allpkg AS (
  -- freshly inserted packages (invisible to a same-statement table scan) ∪ pre-existing ones
  SELECT id, slug FROM ins_pkg
  UNION
  SELECT p.id, p.slug
  FROM public.knowledge_packages p
  JOIN data d ON d.slug = p.slug
  WHERE p.organization_id IS NULL AND p.deleted_at IS NULL
),
changed AS (
  SELECT a.id AS package_id, d.*
  FROM allpkg a
  JOIN data d ON d.slug = a.slug
  WHERE NOT EXISTS (
    SELECT 1
    FROM public.knowledge_package_versions v
    JOIN public.knowledge_localizations le ON le.version_id = v.id AND le.language = 'en'
    JOIN public.knowledge_localizations ls ON ls.version_id = v.id AND ls.language = 'es'
    WHERE v.package_id = a.id AND v.is_current
      AND le.title = d.title_en AND le.body = d.body_en
      AND ls.title = d.title_es AND ls.body = d.body_es
  )
),
demoted AS (
  UPDATE public.knowledge_package_versions v
  SET is_current = false
  WHERE v.is_current AND v.package_id IN (SELECT package_id FROM changed)
  RETURNING v.id
),
retired_chunks AS (
  UPDATE public.knowledge_chunks c
  SET deleted_at = now()
  WHERE c.deleted_at IS NULL AND c.version_id IN (SELECT id FROM demoted)
  RETURNING c.id
),
ver AS (
  INSERT INTO public.knowledge_package_versions
    (package_id, organization_id, version_no, is_current, confidence_tier, source_refs)
  SELECT
    c.package_id, NULL,
    coalesce((SELECT max(vv.version_no) FROM public.knowledge_package_versions vv
              WHERE vv.package_id = c.package_id), 0) + 1,
    true, ${q(TIER)}, jsonb_build_array(c.source_ref)
  FROM changed c
  -- force 'demoted'/'retired_chunks' to run first (one-current partial index)
  WHERE (SELECT count(*) FROM demoted) >= 0
    AND (SELECT count(*) FROM retired_chunks) >= 0
  RETURNING id, package_id
),
loc AS (
  INSERT INTO public.knowledge_localizations
    (version_id, package_id, organization_id, language, title, body)
  SELECT v.id, v.package_id, NULL, x.language, x.title, x.body
  FROM ver v
  JOIN changed c ON c.package_id = v.package_id
  CROSS JOIN LATERAL (VALUES
    ('en', c.title_en, c.body_en),
    ('es', c.title_es, c.body_es)
  ) AS x(language, title, body)
  RETURNING id, version_id, package_id, organization_id, language, body
)
INSERT INTO public.knowledge_chunks
  (localization_id, version_id, package_id, organization_id, language,
   ordinal, body, content_hash, index_status)
SELECT
  l.id, l.version_id, l.package_id, l.organization_id, l.language,
  0, l.body, encode(digest(l.body, 'sha256'), 'hex'), 'pending'
FROM loc l;
`;
}

// ── Staleness check (CI) ────────────────────────────────────────────────────
const sha = (p) => createHash("sha256").update(readFileSync(p)).digest("hex");

function sourceHashes(sheets) {
  const out = {};
  for (const s of sheets) {
    out[s.slug] = {};
    for (const src of s.sources) {
      const p = join(ROOT, src);
      out[s.slug][src] = existsSync(p) ? sha(p) : "MISSING";
    }
  }
  return out;
}

// ── Main ────────────────────────────────────────────────────────────────────
const args = process.argv.slice(2);
const sheets = loadSheets();
console.error(`[app-knowledge] parsed ${sheets.length} screen sheets`);

if (args.includes("--check")) {
  if (!existsSync(HASHES_FILE)) {
    console.error("[app-knowledge] no source-hashes.json — run --update-hashes first");
    process.exit(1);
  }
  const recorded = JSON.parse(readFileSync(HASHES_FILE, "utf8"));
  const current = sourceHashes(sheets);
  const stale = [];
  for (const slug of Object.keys(current)) {
    const rec = recorded[slug];
    if (!rec) { stale.push(`${slug} (new sheet, hashes not recorded)`); continue; }
    for (const [src, h] of Object.entries(current[slug])) {
      if (rec[src] !== h) { stale.push(`${slug} (${src} changed)`); break; }
    }
  }
  if (stale.length) {
    console.error(`[app-knowledge] ${stale.length} STALE sheet(s) — regenerate them:\n  - ${stale.join("\n  - ")}`);
    process.exit(1);
  }
  console.error("[app-knowledge] all sheets in sync with source code ✔");
  process.exit(0);
}

if (args.includes("--update-hashes")) {
  writeFileSync(HASHES_FILE, JSON.stringify(sourceHashes(sheets), null, 2) + "\n", "utf8");
  console.error(`[app-knowledge] wrote ${HASHES_FILE}`);
  process.exit(0);
}

const sql = buildSql(sheets);
const outIdx = args.indexOf("--out");
if (outIdx >= 0 && args[outIdx + 1]) {
  writeFileSync(args[outIdx + 1], sql, "utf8");
  console.error(`[app-knowledge] wrote ${args[outIdx + 1]} (${sheets.length} packages)`);
} else {
  process.stdout.write(sql);
}
